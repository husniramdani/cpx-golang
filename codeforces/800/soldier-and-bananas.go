package main

import "fmt"

func main() {
	var k, n, w, cost int
	fmt.Scan(&k, &n, &w)
	for i := 1; i <= w; i++ {
		cost += i * k
	}
	if cost > n {
		fmt.Println(cost - n)
	} else {
		fmt.Println(0)
	}
}

